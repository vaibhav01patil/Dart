void main() {
  int num = 10;
  if (num == 0) {
    print("zero");
  } else if (num == 1) {
    print("one");
  } else if (num == 2) {
    print("two");
  } else if (num == 3) {
    print("three");
  } else if (num == 4) {
    print("four");
  } else if (num == 5) {
    print("five");
  } else {
    print("$num is greater than 5");
  }
}
